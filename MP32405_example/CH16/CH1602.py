import turtle           # 匯入海龜模組

turtle.setup(280, 150)  # 產生280 X 150畫布
turtle.speed(1)         # 把畫筆速度變慢

turtle.forward(60)      # 畫筆從原點前進50
turtle.left(45)         # 畫筆左轉45度
turtle.forward(60)
turtle.right(45)        # 畫筆向右轉45度
turtle.backward(60)     # 畫筆後退 60
turtle.home()           # 畫筆回到原點
